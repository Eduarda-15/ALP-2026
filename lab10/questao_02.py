for variavel in range(10):
    print(variavel)
#A saída do código mostrou todos os números menores que 10, início=0 fim=10 pulo=1

for variavel in range(1, 5):
    print(variavel)
#A saída do código deu os números de 1 a 4, inicio=1, fim=5, pulo=1

for variavel in range(0, 10, 2):
    print(variavel)
#A saída do código deu apenas os números pares de 0 a 10, pulando de 2 em 2, inicio=0, fim=10, pulo=2

for variavel in range(10, 0, -1):
    print(variavel)
#A saída deu os números de 10 a 1, tirando o 0, inicio=10, fim=0, pulo=-1
